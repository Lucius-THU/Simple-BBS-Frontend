<template>
    <div class="create">
        <div class="container">
            <form>
                <div class="row">
                    <label>标题</label>
                    <input v-model="title">
                    <button type="button" @click.prevent="preview">预览</button>
                    <button type="submit" @click.prevent="submit">发布</button>
                </div>
                <div v-show="!show" v-html="display(content)"></div>
                <textarea v-show="show" id="area" v-model="content"></textarea>
            </form>
        </div>
    </div>
</template>

<script>
import marked from 'marked'
import hljs from 'highlight.js'
import 'highlight.js/styles/googlecode.css'

marked.setOptions({
    breaks: true,
    highlight: function(code) {
        return hljs.highlightAuto(code).value;
    }
})

export default {
    name: 'Create',
    data(){
        return {
            title: "",
            content: "",
            show: true
        }
    },
    mounted(){
        if(this.$route.path !== '/create'){
            this.axios.get('/api/v1/post/' + this.$route.params.postid).then(response => {
                this.title = response.data.title
                this.content = response.data.content
            })
        }
    },
    methods: {
        preview(){
            this.show = !this.show
        },
        submit(){
            if(this.$route.path === '/create'){
                this.axios.post('/api/v1/post', {
                    title: this.title,
                    content: this.content
                }).then(response => {
                    this.$router.replace('/post/' + response.data.postId)
                }).catch(error => {
                    if(error.response.status === 401) this.$router.push('/login')
                })
            } else {
                this.axios.put('/api/v1/post/' + this.$route.params.postid, {
                    title: this.title,
                    content: this.content
                }).then(() => {
                    this.$router.replace('/post/' + this.$route.params.postid)
                }).catch(error => {
                    if(error.response.status === 401) this.$router.push('/login')
                })
            }
        },
        display(content){
            return '<div class="setSize">' + marked(content) + '</div>'
        }
    }
}
</script>

